    public void rebuildSemesterComboBoxes()
    {
        ArrayList<String> semesters = SemesterQueries.getSemesterList();
        currentSemesterComboBox.setModel(new javax.swing.DefaultComboBoxModel(semesters.toArray()));
        if (semesters.size() > 0)
        {
            currentSemesterLabel.setText(semesters.get(0));
            currentSemester = semesters.get(0);
        }
        else
        {
            currentSemesterLabel.setText("None, add a semester.");
            currentSemester = "None";
        }
    }
    private void rebuildCurrStuCourseComboBox()
    {
        String combo = selectStudent.getSelectedItem().toString();
        String lastname = combo.split(",")[0];
        String firstname = combo.split(",")[1];
        
        String studentid = StudentQueries.getOneStudent(firstname, lastname).get(0).getStudentID();
       
        ArrayList<ScheduleEntry> courses = ScheduleQueries.getScheduleByStudent(currentSemesterComboBox.getSelectedItem().toString(), studentid);
        
        String[] arr = new String[courses.size()];
        
        
        int i = 0;
        
        for (ScheduleEntry course : courses)
        {
            arr[i] = course.getCourseCode();
            i++;
        }
        dropCourseCombo.setModel(new javax.swing.DefaultComboBoxModel(arr));
    }

