    private void dropStudentButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_dropStudentButtonActionPerformed
        String combo = dropStudentCombo.getSelectedItem().toString();
        String lastname = combo.split(",")[0];
        String firstname = combo.split(",")[1];
        
        ArrayList<StudentEntry> student = StudentQueries.getOneStudent(firstname, lastname);
        String studentid = student.get(0).getStudentID();
        
        dropStudentTextArea.setText("");
        StudentEntry currStudent = StudentQueries.getStudent(studentid);
        dropStudentTextArea.append(dropStudentCombo.getSelectedItem().toString() +" " + studentid+ " has been dropped from the list of stuents.\n\n");

        for (String currSemester : SemesterQueries.getSemesterList())
        {
            String drop = "";
            String sched = "";
            for (ScheduleEntry curr : ScheduleQueries.getScheduleByStudent(currSemester, currStudent.getStudentID()))
            {
                ScheduleQueries.dropStudentScheduleByCourse(currSemester, currStudent.getStudentID(), curr.getCourseCode());
                if(curr.getStatus().equals("w")){
                    drop += (dropStudentCombo.getSelectedItem().toString() +" has been dropped from the waitlist for " + curr.getCourseCode() + "\n");
                
                }
                else{
                    drop += (dropStudentCombo.getSelectedItem().toString() +" has been dropped from "+curr.getCourseCode() + "\n");
               
                }                
                ArrayList<ScheduleEntry> wait = ScheduleQueries.getWaitlistedStudentsByCourse(currentSemesterComboBox.getSelectedItem().toString(), curr.getCourseCode());
                if (wait.size() > 0)
                {
                    ScheduleQueries.updateScheduleEntry(currSemester, wait.get(0));
                    
                    StudentEntry waitlistedStu = StudentQueries.getStudent(wait.get(0).getStudentID());
                    
                    sched += waitlistedStu.getLastName() + ", " + waitlistedStu.getLastName() + " " + wait.get(0).getStudentID() + " has been scheduled into " + wait.get(0).getCourseCode() + "\n";
                }
            }
            if (!sched.isEmpty()||!drop.isEmpty())
            {
                dropStudentTextArea.append("For Semester: " + currSemester + "\n");
                dropStudentTextArea.append(drop);
                dropStudentTextArea.append(sched);
            }
        }
        
        StudentQueries.dropStudent(studentid);
        rebuildStudentComboBoxes();  
        rebuildStudentComboBoxes();   
        rebuildSelectStuComboBoxes(); 
        rebuildCurrStuCourseComboBox();

    }//GEN-LAST:event_dropStudentButtonActionPerformed

