    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        ArrayList<ScheduleEntry> scheduled = ScheduleQueries.getScheduledStudentsByCourse(currentSemesterComboBox.getSelectedItem().toString(), displayCombo.getSelectedItem().toString());
        ArrayList<ScheduleEntry> waitlisted = ScheduleQueries.getWaitlistedStudentsByCourse(currentSemesterComboBox.getSelectedItem().toString(), displayCombo.getSelectedItem().toString());

        
        DefaultTableModel table = (DefaultTableModel)displayScheduled.getModel();
        table.setNumRows(0);
        Object[] data = new Object[3];
        for (ScheduleEntry scheduleEntry : scheduled)
        {
            StudentEntry curr = StudentQueries.getStudent(scheduleEntry.getStudentID());
            data[0] = curr.getLastName();
            data[1] = curr.getFirstName();
            data[2] = scheduleEntry.getStudentID();
            table.addRow(data);
        }

        table = (DefaultTableModel)displayWaitlisted.getModel();
        table.setNumRows(0);
        data = new Object[3];
        for (ScheduleEntry scheduleEntry : waitlisted)
        {
            StudentEntry student = StudentQueries.getStudent(scheduleEntry.getStudentID());
            data[0] = student.getLastName();
            data[1] = student.getFirstName();
            data[2] = scheduleEntry.getStudentID();
            table.addRow(data);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

