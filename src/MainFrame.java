    private void scheduleCourseSubmitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_scheduleCourseSubmitButtonActionPerformed
        String status = "";
        String cSemester = currentSemesterComboBox.getSelectedItem().toString();
        String cCourse = jComboBox2.getSelectedItem().toString();
        


        if (CourseQueries.getCourseSeats(cSemester, cCourse) > ScheduleQueries.getScheduledStudentCount(cSemester, cCourse))
        {
            status = "s";
        }
        else
        {
            status = "w";
        }
        
        String finalStatus = "";
        if(status.equals("w")){
            finalStatus = "Waitlisted";
        }
        else{
            finalStatus = "Scheduled";
        }
        
        String course = jComboBox2.getSelectedItem().toString();
        String combo = selectStudent.getSelectedItem().toString();
        String lastname = combo.split(",")[0];
        String firstname = combo.split(",")[1];
        
        ArrayList<StudentEntry> student = StudentQueries.getOneStudent(firstname, lastname);
        String studentid = student.get(0).getStudentID();
          ScheduleQueries.addScheduleEntry(new ScheduleEntry(currentSemesterComboBox.getSelectedItem().toString(), course, studentid, status,new java.sql.Timestamp(Calendar.getInstance().getTime().getTime())));

          jLabel4.setText("Course "+course+" has been added for "+combo +" with status "+finalStatus);
          rebuildCurrStuCourseComboBox();
    }//GEN-LAST:event_scheduleCourseSubmitButtonActionPerformed

